list =['f','r','u','i','t','s','f','o','r','l','i','f','e']
print("initial list:")
print(list)


sliced_list=list[4:9]
print("\n slicing elements in the range of 4-9:")
print(sliced_list)
sliced_list=list[6:]
print("\n element sliced from 6th "
      "element till the end:")
print(sliced_list)


